using CONST = Bogus.CLI.Core.Constants;
using Bogus.CLI.Core.Helpers.Interface;
using Bogus.CLI.Core.Services.Interface;

namespace Bogus.CLI.Core.Services.Datasets;
public class DatasetService(
    IDatasetHelper datasetHelper,
    IFakerService fakerService,
    IAddressDatasetService addressDatasetService,
    ICommerceDatasetService commerceDatasetService,
    ICompanyDatasetService companyDatasetService,
    IDateDatasetService dateDatasetService,
    IDatabaseDatasetService databaseDatasetService,
    IFinanceDatasetService financeDatasetService,
    IHackerDatasetService hackerDatasetService,
    IInternetDatasetService internetDatasetService,
    ILoremDatasetService loremDatasetService,
    INameDatasetService nameDatasetService,
    IPhoneDatasetService phoneDatasetService,
    IRantDatasetService rantDatasetService,
    IVehicleDatasetService vehicleDatasetService) : IDatasetService
{
    private readonly IDatasetHelper _datasetHelper = datasetHelper;
    private readonly IFakerService _fakerService = fakerService;
    private readonly IAddressDatasetService _addressDatasetService = addressDatasetService;
    private readonly ICommerceDatasetService _commerceDatasetService = commerceDatasetService;
    private readonly ICompanyDatasetService _companyDatasetService = companyDatasetService;
    private readonly IDateDatasetService _dateDatasetService = dateDatasetService;
    private readonly IDatabaseDatasetService _databaseDatasetService = databaseDatasetService;
    private readonly IFinanceDatasetService _financeDatasetService = financeDatasetService;
    private readonly IHackerDatasetService _hackerDatasetService = hackerDatasetService;
    private readonly IInternetDatasetService _internetDatasetService = internetDatasetService;
    private readonly ILoremDatasetService _loremDatasetService = loremDatasetService;
    private readonly INameDatasetService _nameDatasetService = nameDatasetService;
    private readonly IPhoneDatasetService _phoneDatasetService = phoneDatasetService;
    private readonly IRantDatasetService _rantDatasetService = rantDatasetService;
    private readonly IVehicleDatasetService _vehicleDatasetService = vehicleDatasetService;

    public void ExecuteCommand(
        string[] datasets,
        int count,
        string? locale,
        Action<List<(string Value, string Alias)>> onGenerateFakedata)
    {
        // Controls the randomness of the values generated by the Bogus library.
        Randomizer.Seed = new Random();

        _fakerService.LocaleCode = locale ?? string.Empty;

        if (count <= 0)
            throw new Exception("The option --count must be greater than 1.");

        // Pre-processing: validation and extraction of information from datasets.
        var datasetInfos = new List<(
            string DatasetName,
            string PropertyName,
            string Alias,
            IDictionary<string, object> Parameters)>();

        foreach (var dataset in datasets)
        {
            if (!_datasetHelper.TryParseDataset(
                dataset, out string datasetName, out string propertyName, out string alias, out var parameters))
            {
                throw new Exception($"Invalid format for dataset: {dataset}. Use <dataset.sub-option>.");
            }

            if (!_datasetHelper.DatasetExists(datasetName))
            {
                throw new Exception($"Dataset '{datasetName}' not found.");
            }

            if (!_datasetHelper.PropertyExists(datasetName, propertyName))
            {
                throw new Exception($"The '{datasetName}' does not contain the property '{propertyName}'.");
            }

            datasetInfos.Add((datasetName, propertyName, alias, parameters));
        }

        // Main processing

        for (int i = 0; i < count; i++)
        {
            var row = new List<(string Value, string Alias)>();

            foreach (var (datasetName, propertyName, alias, parameters) in datasetInfos)
            {
                var value = Generate(datasetName, propertyName, parameters);

                if (string.IsNullOrEmpty(value))
                    throw new Exception($"Dataset or property unknown: {datasetName}.{propertyName}");

                row.Add((Value: value, Alias: alias));
            }

            onGenerateFakedata.Invoke(row);
        }
    }

    private string? Generate(
        string datasetName, string propertyName, IDictionary<string, object> parameters) => datasetName switch
    {
        CONST.Datasets.ADDRESS => _addressDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.COMMERCE => _commerceDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.COMPANY => _companyDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.DATE => _dateDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.DATABASE => _databaseDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.FINANCE => _financeDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.HACKER => _hackerDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.INTERNET => _internetDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.LOREM => _loremDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.NAME => _nameDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.PHONE => _phoneDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.RANT => _rantDatasetService.Generate(propertyName, parameters),
        CONST.Datasets.VEHICLE => _vehicleDatasetService.Generate(propertyName, parameters),
        _ => null
    };
}
